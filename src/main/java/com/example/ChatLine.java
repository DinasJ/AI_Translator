package com.example;

public class ChatLine
{
    public final String text;
    public final int color;
    public final int x;
    public final int y;

    public ChatLine(String text, int color, int x, int y)
    {
        this.text = text;
        this.color = color;
        this.x = x;
        this.y = y;
    }
}
